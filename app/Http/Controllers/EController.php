    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $rules = array(
            'e_first_name'    =>  'required',
            'e_last_name'     =>  'required',
            'e_net'           =>  'required',
            'e_brutto'        =>  'required',
            'e_yearlybrut'    =>  'required',
            'e_image'         =>  'image|max:2048'
        );

        

        $error = Validator::make($request->all(), $rules);

        if($error->fails())
        {
            return response()->json(['errors' => $error->errors()->all()]);
        }

        $image = $request->file('e_image');

        $new_name = rand() . '.' . $image->getClientOriginalExtension();

        $image->move(public_path('images'), $new_name);

        $form_data = array(
            'e_first_name'        =>  $request->e_first_name,
            'e_last_name'         =>  $request->e_last_name,
            'e_net'               =>  $request->e_net,
            'e_brutto'            =>  $request->e_brutto,
            'e_yearlybrut'        =>  $request->e_yearlybrut,
            'e_image'             =>  $new_name
        );

        ECrud::create($form_data);

        return response()->json(['success' => 'Data Added successfully.']);
    }

